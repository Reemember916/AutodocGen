/* ***************************************************************** */
/*
 * 本地宏定义
 */
/* ***************************************************************** */

#define WDOG_KEY_1          (0x0055)        //看门狗喂狗参数1
#define WDOG_KEY_2          (0x00AA)        //看门狗喂狗参数2

#define WDOG_WDCR_CHECK     (0x05U << 3)     //写WDCR寄存器校验值
#define WDOG_WDCR_DISAB     (0x01U << 6)     //看门狗禁止
#define WDOG_WDCR_ENAB      (0x00U << 6)     //看门狗使能

/* ******************************************************************/
/**
 * 【函数名称】:wDogEnable
 *
 * 【功能描述】:本函数为看门狗使能函数。需要设置看门狗工作模式，以及喂狗超时时间。
 *
 * 【输入参数】:time ---- 看门狗超时时间设置，可能取值如下：
 *                  WDOG_TIME_4_MS   ---- 看门狗超时时间为4ms
 *                  WDOG_TIME_9_MS   ---- 看门狗超时时间为9ms
 *                  WDOG_TIME_17_MS  ---- 看门狗超时时间为17ms
 *                  WDOG_TIME_35_MS  ---- 看门狗超时时间为35ms
 *                  WDOG_TIME_70_MS  ---- 看门狗超时时间为70ms
 *                  WDOG_TIME_139_MS ---- 看门狗超时时间为139ms
 *                  WDOG_TIME_279_MS ---- 看门狗超时时间为279ms
 *             mode ---- 看门狗工作模式，可能取值如下：
 *                  WDOG_MODE_RESET ---- 复位模式，看门狗超时后，将产生CPU复位
 *                  WDOG_MODE_WDINT ---- 中断模式，看门狗超时后，将产生WDENINT中断
 *
 * 【输出参数】:NONE
 *
 * 【其他说明】:NONE
 *
 * 【返回】    :NONE
 */
/* ******************************************************************/

void wDogEnable(Uint8 time,Uint8 mode)
{
    EALLOW;

    /* 设置看门狗工作模式，复位/产生中断 */
    SysCtrlRegs.SCSR.all = (mode << 1U);

    /* 当看门狗工作于中断模式时，使能中断 */
    if( mode == WDOG_MODE_WDINT )
    {
        PieCtrlRegs.PIEIER1.bit.INTx8 = 1;
        IER |= M_INT1;

        //TODO
        //设置看门狗中断ISR程序
    }

    /* 看门狗寄存器配置为：启用看门狗 ；并配置 看门狗周期 */
    SysCtrlRegs.WDCR = WDOG_WDCR_ENAB | WDOG_WDCR_CHECK | (time & 0x07);

    EDIS;
}

/* ******************************************************************/
/**
 * 【函数名称】:wDogDisable
 *
 * 【功能描述】:本函数为看门狗禁止函数。
 *
 * 【输入参数】:NONE
 *
 * 【输出参数】:NONE
 *
 * 【其他说明】:NONE
 *
 * 【返回】    :NONE
 */
/* ******************************************************************/

void wDogDisable(void)
{
    EALLOW;

    /* 看门狗寄存器配置为：禁用看门狗 */
    SysCtrlRegs.WDCR = WDOG_WDCR_DISAB | WDOG_WDCR_CHECK;

    EDIS;
}

/* ******************************************************************/
/**
 * 【函数名称】:wDogFeed
 *
 * 【功能描述】:本函数为看门狗喂狗函数。依次往WDKEY寄存器中写入0x55、0xAA将会
 *
 *      清除看门狗计数。
 *
 * 【输入参数】:NONE
 *
 * 【输出参数】:NONE
 *
 * 【其他说明】:NONE
 *
 * 【返回】    :NONE
 */
/* ******************************************************************/
#pragma CODE_SECTION(wDogFeed,"ramfuncs");
void wDogFeed(void)
{
    EALLOW;

    SysCtrlRegs.WDKEY = WDOG_KEY_1;
    SysCtrlRegs.WDKEY = WDOG_KEY_2;

    EDIS;
}

/* ***************************************************************** */
/**
 *    [函数名]	 WDogResetTrigger
 *
 *    [功能描述]	 配置最短超时的软件看门狗复位。
 *    			当前硬件上写非法WDCHK不会同步立刻复位CPU，因此这里统一切到
 *    			看门狗复位模式，并将超时时间配置为最短档，随后由调用方停止喂狗。
 *    [输入参数说明] NONE
 *	  [输出参数说明] NONE
 *    [其他说明]	  适用于ISR等不允许死循环等待的场景。
 *    [返回]		  NONE
 */
/* ***************************************************************** */
void WDogResetTrigger(void)
{
    EALLOW;

    /* 按整体写方式切到看门狗复位模式，避免SCSR位写的读改写副作用。 */
    SysCtrlRegs.SCSR.all = (WDOG_MODE_RESET << 1U);

    /* 使能最短超时档位的看门狗；调用方停止喂狗后，硬件将在最短窗口内完成复位。 */
    SysCtrlRegs.WDCR = WDOG_WDCR_ENAB | WDOG_WDCR_CHECK | WDOG_TIME_4_MS;

    EDIS;
}

/* ***************************************************************** */
/**
 *    [函数名]	 WDogReset
 *
 *    [功能描述]	 看门狗复位
 *    			实现软件看门狗复位功能。
 *    			将看门狗切到最短超时复位模式后，进入死循环等待硬件真正复位。
 *    [输入参数说明] NONE
 *	  [输出参数说明] NONE
 *    [其他说明]	  NONE
 *    [返回]		  NONE
 */
/* ***************************************************************** */
void WDogReset(void)
{
    WDogResetTrigger();

    /* 进入死循环，等待看门狗复位  */
    while(1)
    {
    	/* no deal to do  */
    }
}

/* ***************************************************************** */
/**
 *    [函数名]	 WDogWDFlagGet
 *
 *    [功能描述]	 看门狗复位标志获取
 *    [输入参数说明] NONE
 *	  [输出参数说明] NONE
 *    [其他说明]	 NONE
 *    [返回]		 复位标志，取值如下：
 *    			WDOG_WDFLAG_INVALID ---- 喂狗复位标志无效
 *    			WDOG_WDFLAG_VALID   ---- 喂狗复位标志有效
 */
/* ***************************************************************** */
Uint16 WDogWDFlagGet(void)
{
	Uint16 l_WDFlag_u16 = 0U;  /* 喂狗复位标志 */

    /* 看门狗复位标志有效时 */
	if( WDOG_WDFLAG_RESET_BIT == (SysCtrlRegs.WDCR  &  WDOG_WDFLAG_RESET_BIT) )
	{
		/* 复位标志有效 */
		l_WDFlag_u16 = WDOG_WDFLAG_VALID;
	}
	else /* 看门狗复位标志无效时 */
	{
		/* 复位标志无效 */
		l_WDFlag_u16 = WDOG_WDFLAG_INVALID;
	}

	return l_WDFlag_u16;
}

