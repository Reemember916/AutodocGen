#ifndef CONTROL_OUTPUT_H_

#define CONTROL_OUTPUT_H_

#include "Global.h"

/* KZZZ 发送缓存相关私有宏定义 */
#define KZZZ_TIME_REQUEST_SIDE_LEFT   (0x0001U) /* 左吊舱请求当前时间。 */
#define KZZZ_TIME_REQUEST_SIDE_RIGHT  (0x0002U) /* 右吊舱请求当前时间。 */
#define KZZZ_PERIODIC_SEND_DIV        (4U)      /* 50ms调度下累计4拍，对齐ICD 200ms周期量。 */

/* KZZZ 周期/事件发送去重缓存类型定义 */
typedef struct
{
    Uint16 periodicDiv_u16;        /* 周期发送分频计数，用于把50ms调度折算成200ms周期量。 */
    Uint16 lastMbitCmd_u16;        /* 上次已发送的维护BIT命令，避免重复发送事件量。 */
    Uint16 lastPzValid_u16;        /* 上次已发送的配置请求有效位。 */
    Uint16 lastLpPreFuel_u16;      /* 上次已发送的左吊舱预选油量（按100kg量化后）。 */
    Uint16 lastRpPreFuel_u16;      /* 上次已发送的右吊舱预选油量（按100kg量化后）。 */
    Uint16 lastLifeLeft_u16;       /* 上次已发送的左吊舱寿命信息请求位。 */
    Uint16 lastLifeRight_u16;      /* 上次已发送的右吊舱寿命信息请求位。 */
    Uint16 lastOilResetLeft_u16;   /* 上次已发送的左吊舱油量清零请求位。 */
    Uint16 lastOilResetRight_u16;  /* 上次已发送的右吊舱油量清零请求位。 */
} KZZZTxCache_t;

#endif /* end of include guard: CONTROL_OUTPUT_H_ */

